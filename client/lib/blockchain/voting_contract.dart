import 'dart:convert';
import 'package:web3dart/web3dart.dart';
import '../config/env.dart';
import 'web3_client.dart';

class VotingContract {
  late DeployedContract _contract;

  VotingContract() {
    _contract = DeployedContract(
      ContractAbi.fromJson(_abi, "Voting"),
      EthereumAddress.fromHex(Env.contractAddress),
    );
  }

  Future<List<dynamic>> getCandidates() async {
    final function = _contract.function("getCandidates");
    return await web3client.call(
      contract: _contract,
      function: function,
      params: [],
    );
  }
}

